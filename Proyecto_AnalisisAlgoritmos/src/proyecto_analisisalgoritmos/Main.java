    private void IniciarKnapsack() {
        JOptionPane.showMessageDialog(this, "Iniciemos el robo");
        Knapsack_Elemento Nuevo_Elemento;
        Knapsack_Mochila Nueva_Mochila;
        int ValorTotal;
        int PesoTotal;
        int EsPeso; //respuesta 1 si es por peso
        int Numero_Mochilas = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la cantidad de mochilas que desea llevar para este robo:"));
        for (int i = 1; i <= Numero_Mochilas; i++) {
            EsPeso = Integer.parseInt(JOptionPane.showInputDialog("Ingrese si la mochila " + i + " es por \n 1. Peso \n 2. Valor"));
            if (EsPeso == 1) {
                PesoTotal = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el peso maximo de la mochila " + i));
                Nueva_Mochila = new Knapsack_Mochila(PesoTotal, 0, true);
                this.Multiples_Mochilas.add(Nueva_Mochila);
            } else {
                if (EsPeso == 2) {
                    ValorTotal = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el valor maximo de la mochila " + i));
                    Nueva_Mochila = new Knapsack_Mochila(0, ValorTotal, false);
                    this.Multiples_Mochilas.add(Nueva_Mochila);
                } else {
                    JOptionPane.showMessageDialog(this, "No ha ingresado una opcion valida");
                    i--;
                }
            }
        }
        boolean robar = true;
        boolean ingresado = false;
        int Pos_Mochila = 0;
        while (robar) { //Entramos a la casa y vemos que podemos robar
            EsPeso = Integer.parseInt(JOptionPane.showInputDialog("UN ARTICULO!, quieres tomarlo por su: \n 1. Peso \n 2. valor \n 3. Salir Corriendo"));
            ingresado = false;
            Pos_Mochila = 0;
            //verificamos lo que queremos robar y en seguida comprobamos si lo podemos robar
            //Si lo podemos robar entonces lo metemos en la primera mochila que quepa y seguimos
            if (EsPeso == 1) {
                //Verifica insertar por peso
                PesoTotal = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el peso del articulo: "));
                Nuevo_Elemento = new Knapsack_Elemento(PesoTotal, 0);
                while (!ingresado) {
                    if (this.Multiples_Mochilas.get(Pos_Mochila).Es_Peso) {
                        if (this.Multiples_Mochilas.get(Pos_Mochila).NuevoArticulo(Nuevo_Elemento)) {
                            ingresado = true;
                            Pos_Mochila = 0;
                        } else {
                            Pos_Mochila++;
                            JOptionPane.showMessageDialog(null, "No se puede agregar el elemento a la mochila " + Pos_Mochila + ", sobrepasa el peso, se sigue buscando");
                        }
                    }
                    if (Pos_Mochila == this.Multiples_Mochilas.size()) {
                        JOptionPane.showMessageDialog(this, "Ya no le queda espacio en ninguna mochila");
                        ingresado = true;
                    }else{
                        Pos_Mochila++;
                    }
                }
            } else {
                //Verifica insertar por valor
                if (EsPeso == 2) {
                    ValorTotal = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el  valor del articulo: "));
                    Nuevo_Elemento = new Knapsack_Elemento(0, ValorTotal);
                    while (!ingresado) {
                        if (!this.Multiples_Mochilas.get(Pos_Mochila).Es_Peso) {
                            if (this.Multiples_Mochilas.get(Pos_Mochila).NuevoArticulo(Nuevo_Elemento)) {
                                ingresado = true;
                                Pos_Mochila = 0;
                            } else {
                                Pos_Mochila++;
                                JOptionPane.showMessageDialog(null, "No se puede agregar el elemento a la mochila " + Pos_Mochila + ", sobrepasa el valor, se sigue buscando");
                            }
                        }
                        if (Pos_Mochila == this.Multiples_Mochilas.size()) {
                            JOptionPane.showMessageDialog(this, "Ya no le queda espacio en ninguna mochila");
                            ingresado = true;
                        }Pos_Mochila++;
                    }
                } else {
                    //Si escuchamos un ruido nos vamos y estando a salvo hacemos conteo de lo que hemos robado
                    if (EsPeso == 3) {
                        JOptionPane.showMessageDialog(this, "VAMONOS DE AQUI!!!");
                        ingresado = true;
                        robar = false;
                    } else {
                        JOptionPane.showMessageDialog(this, "Ingrese una Opcion valida");
                    }
                }
            }
        }
        Imprimir_Mochilas();
        //Imprimimos
        Reiniciar();
        //vaciamos el contenido para otro atraco :3
    }

    private void Imprimir_Mochilas() {
        //Hacemos la impresion de todo lo que robamos
        String toString = "Articulos en Mochilas \n";
        for (int i = 0; i < this.Multiples_Mochilas.size(); i++) {
            toString += "Mochila: " + i + " \n ";
            toString += this.Multiples_Mochilas.get(i).ElementosDentro();
        }
        JOptionPane.showMessageDialog(this, toString);
    }

    private void Reiniciar() {
        this.Multiples_Mochilas.clear();
    }

